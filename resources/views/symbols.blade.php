    <p class="text-center text-black">Davlat bayrog‘i O‘zbekiston Respublikasining 1991 yil 18 noyabrdagi 407-XII-sonli
        <a class="text-info nav-link" target="_blank" href="http://www.lex.uz/acts/100122">«O‘zbekiston Respublikasining Davlat bayrog‘i to‘g‘risida»gi Qonuni bilan tasdiqlangan.</a>

        O‘zbekiston Respublikasining Davlat bayrog‘i O‘zbekiston Respublikasi davlat suverenitetining ramzidir.

        O‘zbekiston Respublikasining Davlat bayrog‘i xalqaro munosabatlarda O‘zbekiston Respublikasining timsoli bo‘ladi.

        O‘zbekiston Respublikasining Davlat bayrog‘i — bayroqning butun uzunligi bo‘ylab o‘tgan to‘q moviy rang, oq rang va to‘q yashil rangli uchta endan tarkib topgan to‘g‘ri to‘rtburchak shaklidagi matodir.</p>
